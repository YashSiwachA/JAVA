public class IncDec {
    public static void main(String args[]){
        int num1 = 5;
        int num2 = 5;
        System.out.println("Before Incrementing num1: " + num1);
        num1++;
        System.out.println("Ater Incrementing num1: " + num1);
        System.out.println("Before Decrementing num1: " + num2);
        num2--;
        System.out.println("After Decrementing num1: " + num2);

    }
    
}
