public class Switch1stway {

    public static void main(String args[]){
        int n = 5;
        switch(n){
            case(1):
            System.out.println("1st");
            break;
            case(2): 
            System.out.println("2nd");
            break;
            default:
            System.out.println("Not Top Two");      
        }
        
    }
}